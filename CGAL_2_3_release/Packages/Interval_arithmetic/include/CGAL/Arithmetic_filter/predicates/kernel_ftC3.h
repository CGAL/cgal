    FT tsx = tx-sx;
    FT tsy = ty-sy;
    FT tsz = tz-sz;
  
    FT num_x = ps2 * det2x2_by_formula(qsy,qsz,rsy,rsz)
  	   - qs2 * det2x2_by_formula(psy,psz,rsy,rsz);
    FT num_y = ps2 * det2x2_by_formula(qsx,qsz,rsx,rsz)
  	   - qs2 * det2x2_by_formula(psx,psz,rsx,rsz);
